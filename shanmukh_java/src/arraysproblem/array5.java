package arraysproblem;

public class array5 {
    public static void main(String[] args) {
        int [] ar2 = new int [] {1, 2, 3, 4, 5};  
        System.out.println("Elements of given array: ");   
        for (int i = 0; i < ar2.length; i++) {  
            System.out.print(ar2[i] + " ");  
        }   
        System.out.println();   
    }
    
}